/*
this program is to understand the cpp operator (Sizeof())
java and python don't have this operator in it
it will give you the size of a data type in an array
*/

#include <iostream>
using namespace std;

int main(){
    cout<<sizeof(int)<<endl; //size of inteser
    cout<<sizeof(float)<<endl;// size of float
    cout<<sizeof(long long)<<endl;// size of long long
    cout<<sizeof(char)<<endl;// size of char
    return 0;
}